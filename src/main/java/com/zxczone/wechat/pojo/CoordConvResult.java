package com.zxczone.wechat.pojo;

/**
 *
 * @author Jason Zhao
 */
public class CoordConvResult {

    /**
     * longitude
     */
    private String x;
    
    /**
     * latitude
     */
    private String y;

    /**
     * Get longitude
     * @return
     */
    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    /**
     * Get latitude
     * @return
     */
    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
    
}